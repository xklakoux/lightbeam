@RunWith(AndroidJUnit4.class)
@LargeTest

public class LampsScreenTest {

    private static final String NAME1 = "Lampa pierwsza";

    @Rule
    public ActivityTestRule<LampsActivity> mLampsActivityActivityTestRule = new ActivityTestRule<LampsActivity>(LampsActivity.class) {


    };



    private Matcher<View> withItemText(final String itemText) {
        checkArgument(!TextUtils.isEmpty(itemText), "itemText cannot be null or empty");
        return new TypeSafeMatcher<View>() {
            @Override
            public boolean matchesSafely(View item) {
                return allOf(
                        isDescendantOfA(isAssignableFrom(ListView.class)),
                        withText(itemText)).matches(item);
            }

            @Override
            public void describeTo(Description description) {
                description.appendText("is isDescendantOfA LV with text " + itemText);
            }
        };
    }

    public static ViewAction setProgress(final int progress) {
        return new ViewAction() {
            @Override
            public void perform(UiController uiController, View view) {
                SeekBar seekBar = (SeekBar) view;
                seekBar.setProgress(progress);
            }
            @Override
            public String getDescription() {
                return "Set a progress on a SeekBar";
            }
            @Override
            public Matcher<View> getConstraints() {
                return ViewMatchers.isAssignableFrom(SeekBar.class);
            }
        };
    }

    @Test
    public void changeLampName(){
        onView(withText(NAME1)).perform(click());
        onView(withId(R.id.tvName)).perform(click());

        final String newName = "Lampa numer 1";
        onView(withClassName(endsWith("EditText"))).perform(clearText(),typeText(newName),closeSoftKeyboard());
        onView(withText("OK")).perform(click());
        onView(withText(newName)).check(matches(isDisplayed()));
    }

}
